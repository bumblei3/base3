#!/usr/bin/env node
/**
 * Generate validated opening lines by simulating full 3-player games with AI.
 * Outputs the correct format: each line has interleaved moves for all 3 factions.
 */

import { Game } from "./js/game.js";
import { generateBoard } from "./js/board.js";
import {
  calculateBestMove,
  setAIDepth,
  setAIPersonality,
  getAIPersonalities,
} from "./js/ai-core.js";

const PERSONALITIES = getAIPersonalities()
  .map((p) => p.key)
  .filter((p) => p !== "random");
const AI_DEPTH = 5;
const MAX_PLY = 30;
const VARIATIONS_PER_STYLE = 3;

// boardHash is used in other generator scripts, kept for consistency
function _boardHash(game) {
  const pieces = game.pieces
    .filter((p) => p.alive)
    .map((p) => `${p.faction[0]}${p.type[0]}${p.pos.q},${p.pos.r}`)
    .sort()
    .join("|");
  const factionIdx =
    game.currentFactionIdx !== undefined
      ? game.currentFactionIdx
      : ["fire", "water", "nature"].indexOf(game.currentFaction);
  return `${pieces}#${factionIdx}`;
}

function playGameUntilEnd(personality, maxPly = MAX_PLY) {
  const game = new Game();
  game.init(generateBoard());
  game.rpsEnabled = true;
  setAIPersonality(personality);
  setAIDepth(AI_DEPTH);

  const moveHistory = [];
  let moveCount = 0;

  while (moveCount < maxPly && game.state !== "game_over") {
    const action = calculateBestMove(game, game.currentFaction);
    if (!action) break;

    const moveStr = `${action.piece.id} -> ${action.target.q},${action.target.r}`;

    moveHistory.push({
      faction: game.currentFaction,
      moveStr,
    });

    const selResult = game.handleCellClick(action.piece.pos);
    if (!selResult || selResult.action !== "select") break;

    const result = game.handleCellClick(action.target);
    if (result && result.promotion && game.pendingPromotion) {
      game.completePromotion("queen");
    }

    moveCount++;
  }

  return moveHistory; // Array of {faction, moveStr} in order played
}

function mergeVariations(variations) {
  // Find the longest variation as base
  variations.sort((a, b) => b.length - a.length);
  return variations[0]; // Return the longest valid line
}

async function main() {
  console.log(
    "=== Generating Validated Opening Lines (Interleaved Format) ===",
  );
  console.log(
    `AI Depth: ${AI_DEPTH}, Max Ply: ${MAX_PLY}, Variants: ${VARIATIONS_PER_STYLE}\n`,
  );

  const allStyles = {
    Main: "balanced",
    Aggressive: "aggressive",
    Solid: "defensive",
    Flank: "balanced",
    "Knight Rush": "tactical",
  };

  const styleLines = {};

  for (const [styleName, personality] of Object.entries(allStyles)) {
    console.log(`\n--- Generating ${styleName} (${personality}) ---`);

    const variations = [];
    for (let v = 0; v < VARIATIONS_PER_STYLE; v++) {
      const p =
        Math.random() < 0.3 && VARIATIONS_PER_STYLE > 1
          ? PERSONALITIES[Math.floor(Math.random() * PERSONALITIES.length)]
          : personality;

      const history = playGameUntilEnd(p, MAX_PLY);
      variations.push(history);
      console.log(
        `  Var ${v + 1}: ${history.length} total moves (Fire:${history.filter((m) => m.faction === "fire").length}, Water:${history.filter((m) => m.faction === "water").length}, Nature:${history.filter((m) => m.faction === "nature").length})`,
      );
    }

    // Take the longest variation
    const bestLine = mergeVariations(variations);
    styleLines[styleName] = bestLine.map((m) => m.moveStr);
    console.log(`  → Best line: ${bestLine.length} moves`);
  }

  // Build output JSON with correct format
  const output = {
    version: "1.0",
    metadata: {
      generator: "Trischach AI Opening Book Generator",
      created: new Date().toISOString().split("T")[0],
      generatorVersion: "2.0.0",
      game: "TriSchach",
      boardSize: "Hexagonal 9x9",
      factions: ["fire", "water", "nature"],
      maxPly: MAX_PLY,
      description:
        "Opening book generated by AI self-play (depth 5) - all moves validated in 3-player context. Each line contains full move sequence for all 3 factions.",
    },
    lines: {},
  };

  // Each style is a line for ALL factions (same line, just different weights/names per faction perspective)
  for (const [styleName, moves] of Object.entries(styleLines)) {
    for (const faction of ["fire", "water", "nature"]) {
      if (!output.lines[faction]) output.lines[faction] = {};

      let weight = 85;
      if (styleName === "Main") weight = 100;
      else if (styleName === "Aggressive") weight = 95;
      else if (styleName === "Solid") weight = 90;
      else if (styleName === "Flank") weight = 80;
      else if (styleName === "Knight Rush") weight = 85;

      output.lines[faction][styleName] = {
        name: `${faction.charAt(0).toUpperCase() + faction.slice(1)} ${styleName}`,
        description: `${faction} ${styleName.toLowerCase()} - AI validated line (${moves.length} plies, depth ${AI_DEPTH})`,
        weight,
        moves: moves, // ALL moves interleaved!
      };
    }
  }

  // Write
  const fs = await import("fs");
  fs.writeFileSync("opening-book.json", JSON.stringify(output, null, 2));
  console.log("\n✅ Written to opening-book.json");

  // Stats
  let totalMoves = 0;
  for (const faction of ["fire", "water", "nature"]) {
    for (const style of Object.keys(output.lines[faction])) {
      totalMoves += output.lines[faction][style].moves.length;
    }
  }
  console.log(`Styles: ${Object.keys(styleLines).length} × 3 factions`);
  console.log(`Total moves in JSON: ${totalMoves}`);
  console.log(
    `Max ply per line: ${Math.max(...Object.values(styleLines).map((m) => m.length))}`,
  );
}

main().catch(console.error);
